import math
ang=float(input('Digite um 창ngulo: '))
sen=math.sin(math.radians(ang))
cos=math.cos(math.radians(ang))
tan=math.tan(math.radians(ang))
print('O 창ngulo {:.2f} tem como SENNO {:.2f}'.format(ang,sen))
print('O 창ngulp {:.2f} tem como COSENNO {:.2f}'.format(ang,cos))
print('O 창ngulo {:.2f} tem como TANGENTE {:.2f}'.format(ang,tan))